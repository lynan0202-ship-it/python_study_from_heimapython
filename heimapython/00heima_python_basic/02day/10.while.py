"""
关于循环, 另一种比较重要的就是 统计思想了, 例如: 统计水仙花数的个数.

需求1: 打印所有的水仙花数.
需求2: 统计水仙花数的个数.

水仙花数解释:
    1. 它是1个三位数.
    2. 它的各个位数的立方和相加 = 它本身.
    3. 例如: 153 = 1*1*1 + 5*5*5 + 3*3*3 = 153, 所以 153就是水仙花数.
"""


# 需求1: 打印所有的水仙花数
i = 100
while i < 10000:
    # 提取各位数字（缩进4空格，属于while循环内）
    ge = i % 10  # 简化写法：i//1%10等价于i%10
    shi = i // 10 % 10
    bai = i // 100 % 10
    qian = i // 1000 % 10

    # 多行注释（缩进与while内代码一致，不影响执行）
    """
    注释内容...
    """

    # 判断并打印（缩进4空格，属于while循环内）
    if ge ** 3 + shi ** 3 + bai ** 3 +qian ** 3== i:
        print(i)  # 缩进8空格（或4空格的倍数），属于if条件内

    # 控制循环变量（缩进4空格，属于while循环内）
    i += 1

print('-' * 28)  # 无缩进，属于循环外

