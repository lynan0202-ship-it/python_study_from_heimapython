# 1. 定义函数, 介绍自己的基本信息.
def print_info():
    '''
    该函数用于打印 自身的信息
    :return: 无
    '''
    print('你好, 我叫: 夯哥')
    print('我今年 18 岁了')
    print('很高兴认识大家, 祝大家学有所成, 早日高薪!')
    # return  没有返回值, return可以省略不写.

# 2. 调用函数
print_info()

