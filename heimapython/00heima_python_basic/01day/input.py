"""
输入介绍:
    概述:
        输入指的是: 用户键盘录入数据, 我们来接收这些数据.
    格式:
        变量名 = input('提示信息')
    细节:
        无论用户录入什么数据, 我们默认都会当做 字符串来接收.
"""

# 1. 提示用户录入他/她的年龄, 并接收.
age = input('请录入您的年龄: ')

# 下下个知识点讲(类型转换).  把接收到的 数据, 转成 整数
new_age = int(age)      # 把 age变量 转成 int类型数据, 即: '20' -> 20

# 2. 打印上述接收到的变量的 类型.
print(type(age))            # <class 'str'>
print(type(new_age))        # <class 'int'>

# 3. 打印上述接收到的变量值.
print('您的年龄是: %d' % new_age)