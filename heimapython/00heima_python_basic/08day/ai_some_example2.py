from itertools import permutations  # 导入排列生成模块

count = 0
# 生成1-4的所有4个数字的排列（返回元组，比如(1,2,3,4)）
for p in permutations([1,2,3,4], 4):
    '''
    在 Python 中，permutations([1,2,3,4], 4) 是 itertools 模块中的函数，用于生成列表 [1,2,3,4] 中 4 个元素的所有全排列（即所有可能的 4 元素排列组合）。
遍历该迭代器时，变量 p 的类型是 tuple（元组）。
具体说明：
permutations 函数返回的是一个迭代器，其中每个元素都是一个元组，元组中包含指定长度的排列组合。
对于 permutations([1,2,3,4], 4)，生成的每个排列都是由 4 个整数组成的元组，例如 (1,2,3,4)、(1,2,4,3)、(1,3,2,4) 等。
因此，p 作为迭代器中的每个元素，类型为 tuple。
    '''
    s = ''.join(map(str, p))  # 转成字符串，比如"1234"
    '''
这行代码实现的是 **“多元素类型转换 + 序列聚合”的组合操作，具体来说是将一个包含非字符串元素的序列（如元组p）转换为单一字符串，属于“复合类型转换”**。
分步解析转换逻辑
假设p是一个包含整数的元组（如(1,2,3,4)），代码的转换过程分为两步：
第一步：map(str, p)—— 元素级类型转换
map(str, p) 是 Python 的内置函数，作用是将序列p中的每个元素通过str()函数转换为字符串类型。
例如：p = (1,2,3,4) 经过 map(str, p) 后，得到一个迭代器，包含 '1'、'2'、'3'、'4'（每个整数被转换为对应的字符串）。
第二步：''.join(...)—— 序列聚合为单一字符串
''.join(iterable) 是字符串的内置方法，作用是将可迭代对象（如map返回的字符串迭代器）中的所有字符串元素拼接起来，形成一个完整的字符串。
例如：将 ['1','2','3','4'] 拼接为 "1234"（'' 表示拼接时无分隔符）。
    '''
    
    # 同样的检查条件
    if '13' not in s and '31' not in s and s[0] != '4':
        count += 1
        print(s, end='\n' if count%3==0 else '\t')