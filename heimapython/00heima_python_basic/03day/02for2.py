# 定义一个包含多种类型元素的列表
my_list = [10, "hello", True, 3.14, ["a", "b"]]

# for循环遍历列表，临时变量item依次取列表中的每个元素
for item in my_list:
    print(f"当前元素：{item}，类型：{type(item)}")  # 打印元素值和类型

# 定义一个数字列表
numbers = [2, 7, 12, 15, 20, 23, 28]

# 筛选列表中的偶数并打印
print("列表中的偶数：")
for num in numbers:
    if num % 2 == 0:  # 判断是否为偶数（能被2整除）
        print(num)

# 生成从5开始，到20结束（不包含20），步长为3的序列
# 序列为：5, 8, 11, 14, 17（每次加3，直到小于20）
for n in range(5, 20, 3):
    print(n)

# 生成从10到1的倒序序列（步长为-1表示递减）
# 序列为：10,9,8,...,1（注意：结束值需小于起始值，步长为负数）
for num in range(10, 0, -1):
    print(num, end=" ")  # 用空格分隔，不换行
# 用range(3)控制循环执行3次（序列为0,1,2，共3个元素）
for i in range(3):
    print(f"这是第{i+1}次循环")  # i从0开始，+1转为“第n次”