"""
代码名称：函数参数简明示例
知识要点总结：
1. 缺省参数：带默认值的参数，必须放在最后
2. *args：接收多余位置参数，打包为元组
3. **kwargs：接收多余关键字参数，打包为字典

代码总体逻辑说明：
通过一个综合函数演示三种参数的使用方法和规则
"""


def student_info(name, age, city='北京', *scores, **extras):
    """
    学生信息函数 - 综合演示各种参数

    参数:
        name: 姓名(必需)
        age: 年龄(必需)
        city: 城市(缺省参数，默认'北京')
        *scores: 可变位置参数，接收多个成绩
        **extras: 可变关键字参数，接收其他信息
    """
    print(f"姓名: {name}")
    print(f"年龄: {age}")
    print(f"城市: {city}")
    if scores:
        print(f"成绩: {scores}")
    if extras:
        print(f"其他: {extras}")
    print("-" * 20)


if __name__ == '__main__':
    # 基本用法 - 只传必需参数，使用缺省参数默认值
    student_info("张三", 18)

    # 覆盖缺省参数
    student_info("李四", 19, "上海")

    # 使用*args接收多个成绩
    student_info("王五", 20, "广州", 85, 92, 78)

    # 使用**kwargs接收额外信息
    student_info("赵六", 21, "深圳", phone="13800138000", hobby="篮球")

    # 综合使用所有参数
    student_info("孙七", 22, "杭州", 90, 88, 95, email="sun@qq.com", wechat="sunqi")

    student_info("孙七", 22, "杭州", 90, 88, 95, email="sun@qq.com", wechat="sunqi")
