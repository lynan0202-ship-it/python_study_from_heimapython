"""
代码名称：函数参数详解 - 缺省参数与不定长参数
知识要点总结：
1. 缺省参数（默认参数）
   - 定义在形参列表最后，使用 参数名=默认值 格式
   - 调用时可传值也可不传，不传时使用默认值
   - 必须放在非缺省参数之后

2. 不定长参数（可变参数）
   - *args：接收任意个位置参数，封装为元组
   - **kwargs：接收任意个关键字参数，封装为字典
   - 同时使用时顺序：*args在前，**kwargs在后
   - 与缺省参数共存时顺序：*args、缺省参数、**kwargs

代码总体逻辑说明：
1. 演示缺省参数的基本用法和规则
2. 展示*args接收位置参数的机制
3. 展示**kwargs接收关键字参数的机制
4. 演示各种参数混合使用的正确顺序
5. 通过实际调用验证参数传递规则
"""


# ==================== 缺省参数示例 ====================

def user_info(name, age, address='三亚'):
    """
    用户信息函数 - 演示缺省参数用法

    参数:
        name (str): 姓名 - 必填参数
        age (int): 年龄 - 必填参数
        address (str): 地址 - 缺省参数，默认值为'三亚'
    """
    print(f'我叫: {name}, 今年 {age} 岁了, 我住在: {address}')


# ==================== 不定长参数示例 ====================

def method01(*args):
    """
    演示*args接收位置参数

    参数:
        *args: 可变位置参数，接收所有位置参数并封装为元组
    """
    print(f'*args接收到的所有位置参数: {args}')
    print(f'参数类型: {type(args)}')
    print(f'参数个数: {len(args)}')


def method02(**kwargs):
    """
    演示**kwargs接收关键字参数

    参数:
        **kwargs: 可变关键字参数，接收所有关键字参数并封装为字典
    """
    print(f'**kwargs接收到的所有关键字参数: {kwargs}')
    print(f'参数类型: {type(kwargs)}')
    print(f'参数个数: {len(kwargs)}')


def method03(*args, **kwargs):
    """
    演示同时使用*args和**kwargs

    参数:
        *args: 接收位置参数
        **kwargs: 接收关键字参数
    """
    print(f'位置参数args: {args}')
    print(f'关键字参数kwargs: {kwargs}')


def method04(*args, name='张三', **kwargs):
    """
    演示*args、缺省参数、**kwargs的混合使用

    参数:
        *args: 接收位置参数
        name (str): 缺省参数，默认值为'张三'
        **kwargs: 接收关键字参数
    """
    print(f'缺省参数name: {name}')
    print(f'位置参数args: {args}')
    print(f'关键字参数kwargs: {kwargs}')


# ==================== 主程序 ====================

if __name__ == '__main__':
    print("=== 缺省参数演示 ===")

    # 正确：传递所有参数，覆盖缺省值
    user_info('张三', 23, '北京')

    # 错误示例（注释）：位置参数顺序错误 - 虽然不报错但结果混乱
    # user_info('张三', '北京', 23)  # 年龄会显示为'北京'，地址显示为23

    # 正确：使用关键字参数，顺序任意
    user_info(address='广州', name='李四', age=24)

    # 正确：混合使用，位置参数在前
    user_info('王五', age=25, address='深圳')

    # 正确：使用缺省参数默认值
    user_info('赵六', age=26)  # address使用默认值'三亚'

    print("\n" + "=" * 40 + "\n")

    print("=== 不定长参数 *args 演示 ===")

    # *args接收任意个位置参数
    method01(1, '张三', 23)  # 3个位置参数
    method01(10, 20, 'aa', 'bb')  # 4个位置参数

    # 错误示例（注释）：*args不能接收关键字参数
    # method01(1, '张三', age=23)     # 会报错

    print("\n" + "=" * 40 + "\n")

    print("=== 不定长参数 **kwargs 演示 ===")

    # **kwargs接收任意个关键字参数
    method02(name='张三', age=23, phone='13112345678')
    method02(city='北京', country='中国')

    # 错误示例（注释）：**kwargs不能接收位置参数
    # method02('李四', age=24, phone='13112345678')  # 会报错

    print("\n" + "=" * 40 + "\n")

    print("=== *args和**kwargs混合使用演示 ===")

    # 同时传递位置参数和关键字参数
    method03(10, 20, 'aa', name='王五', age=25, address='杭州')
    method03('hello', 'world', x=100, y=200)

    print("\n" + "=" * 40 + "\n")

    print("=== *args、缺省参数、**kwargs混合使用演示 ===")

    # 使用缺省参数的默认值
    method04(10, 20, 'aa', age=25, address='杭州')  # name使用默认值'张三'

    # 覆盖缺省参数的值
    method04(10, 20, 'aa', name='王五', age=25, address='杭州')  # name被覆盖为'王五'

